public class Zookeeper {
	
	//Zookeeper tasks
	
	public void wakeAnimals() {
		System.out.println("The zookeeper is waking up the animals.");
	}
	
	public void rollCall() {
		System.out.println("The zookeeper called roll.");
	}
	
	public void feedAnimals() {
		System.out.println("The zookeeper is feeding the animals.");
	}
	
	public void exerciseAnimals() {
		System.out.println("The zookeeper is giving the animals exercises.");
	}
	
	public void shutDown() {
		System.out.println("The zookeeper is shutting down the zoo.");
	}

}
